    private void jBsalvarPaisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBsalvarPaisActionPerformed
        //variaveis criadas para armazenar informações da tela
        String id = jTidPais.getText();
        String pais = jTnomePais.getText();
        String obrigatorio = "";
        boolean controle = false;
        //validação
        if (id.equals("")) {
            obrigatorio = obrigatorio + "\n - ID";
            controle = true;
        }
        if (pais.equals("")) {
            obrigatorio = obrigatorio + "\n - Nome da Pais";
            controle = true;
        }
        if (controle == false) {
            //insere informaçoes de paises no cadastro do banco
            paisDAO.insertCadastroPais(id, pais);
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Campos obrigatorios:" + obrigatorio);
        }
    }//GEN-LAST:event_jBsalvarPaisActionPerformed


