    private void jBsalvarEstadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBsalvarEstadoActionPerformed
        //variaveis criadas para buscar as informaçoes na tela
        String id = jTidEstado.getText();
        String estado = jTnomeEstado.getText();
        String sigla = jTsiglaEstado.getText();
        String pais = (String) jCBpais.getSelectedItem();
        paisDAO.selectPaisId(pais);
        System.out.println(paisDAO.selectPaisId(pais));
        String obrigatorio = "";
        boolean controle = false;
        //validação
        if (estado.equals("")) {
            obrigatorio = obrigatorio + "\n - Nome do Bairro";
            controle = true;
        }
        if (sigla.equals("")) {
            obrigatorio = obrigatorio + "\n - Nome da Cidade";
            controle = true;
        }
        if (pais.equals("")) {
            obrigatorio = obrigatorio + "\n - Nome da Cidade";
            controle = true;
        }
        if (controle == false) {
            //função que insere informaçoes de cadastro de estado
            estadoDAO.insertCadastroEstado(id, sigla, estado, paisDAO.selectPaisId(pais) );
            dispose();
            
        } else {
            JOptionPane.showMessageDialog(this, "Campos obrigatorios:" + obrigatorio);
        }    }//GEN-LAST:event_jBsalvarEstadoActionPerformed


